/*! \file constants.cpp
 *  \brief Library of mathematical and physical constants
 */

namespace numlib{

//! Ratio of a circle's circumference to its diameter, \f$\pi\f$
extern const double pi = 3.1415926535897931;

//! The product \f$2 \pi\f$
extern const double twopi = 2.0*pi;

//! Conversion from degrees to radians
extern const double deg_to_rad = pi/180.0;

//! Conversion from radians to degrees
extern const double rad_to_deg = 180.0/pi;

}
